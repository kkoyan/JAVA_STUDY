/* 変数宣言の文
変数とはデータを格納するためにコンピュータ内部に準備する箱のような物
数値や文字列など様々な情報の出し入れが可能。

public class Main {
  public static void main(String[] args) {  //変数宣言の文（ageという箱を用意）
    int age;  //変数宣言の文（ageという箱を用意）
    age = 30;  //箱に数字の「３０」を入れる
    System.out.println(age);  //箱の中身を表示
  }
}
実行結果は「３０」が出る。

変数に値を入れることを「代入」、取り出すことを「取得」
どちらも変数宣言後でないとできない。

変数宣言のルール
型  変数名;
型 …変数に入れることのできるデータの種類
例）int は整数を表す型。この型で宣言された変数ageには整数しか入れられない。
（小数、文字列は不可。）

*/